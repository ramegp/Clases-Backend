export const sum = (number1: number, number2: number) => { return number1 + number2 }

export const subtract = (number1: number, number2: number) => { return number1 - number2 }

export const divide = (number1: number, number2: number) => { return number1 / number2 }

export const multiply = (number1: number, number2: number) => { return number1 * number2 }

